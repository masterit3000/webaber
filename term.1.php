				<strong>Điều 4. Quyền và trách nhiệm của chúng tôi</strong>
			</p>
			<ol>
				<li>Công ty có quyền sở hữu tất cả các quyền, quyền sở hữu và lợi ích, bao gồm tất cả các quyền sở hữu tài sản trí tuệ có
					liên quan, đối với Phần mềm và/hoặc Ứng dụng.</li>
				<li>Tuân thủ các quy định của pháp luật liên quan đến việc cung ứng dịch vụ.</li>
				<li>Đảm bảo cung cấp chính xác, đầy đủ các dịch vụ cho Khách hàng theo thỏa thuận.</li>
				<li>Cung cấp tài liệu hướng dẫn, hỗ trợ khách hàng sử dụng dịch vụ.</li>
				<li>Giải đáp thắc mắc, khiếu nại của khách hàng liên quan đến dịch vụ.</li>
				<li>Bảo mật các thông tin của Khách hàng được lưu tại Công ty, trừ trường hợp phải thực hiện cung cấp thông tin theo quy
					định của pháp luật.</li>
				<li>
					Khóa/tạm ngừng/chấm dứt/ hoặc từ chối việc thực hiện giao dịch hoặc tạm ngưng dịch vụ hoặc khóa chức năng đăng nhập của Khách
					hàng trong quá trình sử dụng Dịch vụ mà không cần phải báo trước cho Khách hàng khi xảy ra các trường hợp như sau:
					<ol>
						<li>Tạm ngưng dịch vụ để nâng cấp, bảo trì hệ thống;</li>
						<li>Khi phát hiện Khách hàng vi phạm bất kỳ nội dung nào trong Bản điều khoản điều kiện này, các quy định về việc sử dụng
							Ứng dụng do Công ty ban hành hoặc các quy định pháp luật có liên quan;</li>
						<li>Giao dịch&nbsp;của Khách hàng&nbsp;có dấu hiệu vi phạm pháp luật;</li>
						<li>Theo quyết định và/hoặc yêu cầu của cơ quan nhà nước có thẩm quyền.</li>
					</ol>
				</li>
				<li>Không chịu trách nhiệm về bất kỳ tranh chấp phát sinh nào giữa khách hàng và bên thứ ba (nếu có) phát sinh từ hoặc liên
					quan đến các giao dịch được thực hiện không thông qua dịch vụ của Ứng dụng.</li>
				<li>Thay đổi tài liệu liên quan đến dịch vụ, thay đổi/điều chỉnh thông tin giao diện màn hình, chức năng dịch vụ vào bất
					kỳ thời điểm nào mà không cần phải báo trước cho khách hàng.</li>
				<li>Khi nâng cấp, phát triển dịch vụ, Công ty được phép bổ sung, cung cấp thêm các tiện ích dịch vụ cho Khách hàng mà không
					cần thông báo hoặc yêu cầu Khách hàng đăng ký bổ sung dịch vụ.</li>
				<li>Khi khách hàng truy nhập và sử dụng ứng dụng , Công ty có thể thu thập và lưu trữ các thông tin như các số liệu thống
					kê quá trình truy cập, các thông tin cá nhân cung cấp cho Công ty khi đăng ký… Các thông tin cá nhân khách hàng cung
					cấp bao gồm nhưng không giới hạn bởi tên, ngày sinh, số điện thoại, địa chỉ email, địa chỉ thường trú, địa chỉ sử dụng
					dịch vụ.... Chúng tôi có quyền sử dụng các thông tin này vào việc lập kế hoạch, nghiên cứu, quản lý, giám sát, tư vấn,
					hỗ trợ giữa Công ty với Khách hàng và (hoặc) sử dụng vào mục đích quảng bá, giới thiệu sản phẩm dịch vụ của Công ty
					cho Khách hàng hoặc cung cấp thông tin cho các cơ quan pháp luật theo yêu cầu của Luật pháp hoặc của Toà án.</li>
				<li>Các quyền khác theo quy định của Công ty và của pháp luật.</li>
			</ol>
			<p>
				<strong>Điều 5. Rủi ro và miễn trừ trách nhiệm</strong>
			</p>
			<p>Chúng tôi cam kết cung cấp Dịch vụ tốt nhất và cố gắng giảm thiểu các rủi ro có thể gặp phải về kỹ thuật cho Khách hàng;
				không chịu trách nhiệm về những rủi ro, thiệt hại của Khách hàng khi Khách hàng sử dụng Dịch vụ do những rủi ro sau:</p>
			<ol>
				<li>Lỗi hệ thống phần cứng, phần mềm không hoạt động hoặc hoạt động không đúng chức năng thiết kế do các yếu tố bất khả kháng
					gây ra như lũ lụt, hỏa hoạn, thiên tai, chập điện, hư hỏng tự nhiên …;</li>
				<li>Nguy cơ ngưng trệ hoạt động của hệ thống mạng do lỗi đường truyền Internet, mạng điện thoại di động … của nhà cung
					cấp dịch vụ gặp sự cố kỹ thuật hoặc quá tải;</li>
				<li>Hệ thống có thể bị nhiễm virus, bị tấn công hoặc bị ảnh hưởng của những sự cố ngoài tầm kiểm soát của Công ty làm ngưng
					trệ các hoạt động trao đổi thông tin; dẫn đến việc không thể xử lý hoặc thực hiện các giao dịch của khách hàng;</li>
				<li>Việc nhận dạng có thể bị nhầm, sai sót do các thông tin nhận dạng Khách hàng (tên truy cập, mật khẩu truy cập, số điện
					thoại, địa chỉ thư điện tử) bị bên thứ 3 chiếm đoạt bằng các thủ đoạn bất hợp pháp…;</li>
				<li>Rủi ro lệnh giao dịch một khi đã được Khách hàng gửi đi thì không thể thu hồi do các giao dịch trên hệ thống Ứng dụng
					của chúng tôi được xử lý và thực hiện một cách tự động.</li>
				<li>Khách hàng sẽ chịu trách nhiệm hoàn toàn về các quyết định liên quan đến các dịch vụ và sản phẩm được giới thiệu
					tới khách hàng thông qua dịch vụ, phần mềm và/hoặc ứng dụng. Khách hàng hoàn toàn bãi bỏ và miễn trừ Công ty khỏi
					bất kỳ và mọi trách nhiệm pháp lý, khiếu nại, nguyên nhân khởi tố, hay thiệt hại nào xuất phát từ việc sử dụng dịch
					vụ, phần mềm và/hoặc ứng dụng của khách hàng, hoặc trong bất kỳ cách nào liên quan đến các bên thứ ba đã được giới
					thiệu tới khách hàng thông qua dịch vụ, phần mềm và/hoặc ứng dụng.</li>
			</ol>
			<p>
				<strong>Điều 6. Luật áp dụng và giải quyết tranh chấp</strong>
			</p>
			<ol>
				<li>Điều kiện, điều khoản Dịch vụ này được điều chỉnh bởi pháp luật Việt Nam.</li>
				<li>Trong quá trình thực hiện nếu phát sinh tranh chấp, hai bên sẽ chủ động giải quyết trên cơ sở thương lượng, hòa giải.
					Trường hợp không giải quyết được, tranh chấp sẽ được đưa ra Tòa án có thẩm quyền theo quy định pháp luật để giải quyết,
					trừ trường hợp pháp luật có quy định khác.</li>
			</ol>
			<p>
				<strong>Điều 7. Điều khoản cuối cùng</strong>
			</p>
			<ol>
				<li>Các vấn đề chưa được quy định tại Điều kiện, điều khoản này sẽ được thực hiện theo quy định của pháp luật, hướng dẫn
					của cơ quan Nhà nước có thẩm quyền và/hoặc các cam kết/thỏa thuận có hiệu lực khác giữa các bên.</li>
				<li>Khách hàng đã đọc, hiểu, nhất trí và cam kết thực hiện đúng các quy định tại Điều kiện, điều khoản sữ dụng này.</li>
				<li>Điều kiện, điều khoản này có hiệu lực kể từ ngày Khách hàng thực hiện đăng ký và sử dụng dịch vụ ứng dụng/phần mềm
					của Công ty.</li>
