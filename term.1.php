			<p>
				<strong>Điều 3. Quyền và trách nhiệm của khách hàng</strong>
			</p>
			<ol>
				<li>Đọc, hiểu, tuân thủ các thủ tục đăng ký, trình tự giao dịch và các quy định, hướng dẫn sử dụng và và Bản điều khoản
					điều kiện này trước khi sử dụng dịch vụ của Công ty chúng tôi.</li>
				<li>Sử dụng đúng mục đích những thông tin dịch vụ do Ứng dụng cung cấp.</li>
				<li>Cung cấp đầy đủ, chính xác các thông tin cần thiết khi đăng ký và/hoặc phát sinh trong quá trình sử dụng dịch vụ theo
					yêu cầu của Công ty.</li>
				<li>Tuân thủ các quy định pháp luật và quy định của Công ty liên quan đến việc đăng ký và sử dụng Dịch vụ ứng dụng.</li>
				<li>Không sử dụng Ứng dụng hoặc Phần mềm để gây khó khăn hoặc rối loạn hoạt động hoặc làm giả yêu cầu đặt xe ngoài
					mục đích của dịch vụ.</li>
				<li>Không sao chép, hoặc phân phối Phần mềm hoặc nội dung khác mà không có sự cho phép bằng văn bản của Công ty.</li>
				<li>Giữ an toàn và bảo mật mật khẩu tài khoản của mình hoặc bất kỳ phương thức nhận dạng nào mà chúng tôi cung cấp để bạn
					truy cập vào Dịch vụ.</li>
				<li>Khi khách hàng đã hoàn thành một chuyến đi thông qua Dịch vụ, khách hàng cần phải thanh toán toàn bộ tiền cước cho
					bên cung cấp thứ ba. Khách hàng có thể lựa chọn việc thanh toán dịch vụ bằng tiền mặt, bằng thẻ tín dụng, thẻ ghi
					nợ hoặc bằng thẻ Member card của Công ty chúng tôi.</li>
				<li>Công ty có quyền nhưng không bắt buộc phải chấm dứt Hợp đồng này và chấm dứt việc sử dụng Dịch vụ của bạn bất kỳ lúc
					nào mà có hoặc không thông báo; có quyền ngừng xử lý bất kỳ một giao dịch nào mà chúng tôi cho rằng giao dịch đó có
					thể là lừa đảo, bất hợp pháp hoặc có liên quan tới các hành vi phạm tội hoặc khi chúng tôi cho rằng bạn đang vi phạm
					Điều khoản sử dụng.</li>
				<li>Cập nhật, theo dõi các thông tin, thông báo, quy định, hướng dẫn về sử dụng Dịch vụ của Công ty theo các phương thức
					như: (i) cập nhật trên Ứng dụng; (ii) tài liệu hướng dẫn của Công ty; (iii) thông báo, hướng dẫn qua email, tin nhắn
					sms; (iv) các phương thức khác theo quy định của Công ty trong từng thời kỳ.</li>
				<li>Gửi yêu cầu hỗ trợ, khiếu nại trong quá trình sử dụng Dịch vụ cho Công ty thông qua các hình thức: gọi điện thoại đến
					Tổng đài hỗ trợ dịch vụ khách hàng hoặc hình thức khác theo quy đinh của Công ty.</li>
				<li>Hợp tác và cung cấp thông tin cần thiết theo yêu cầu của chúng tôi phù hợp với thỏa thuận với Công ty và quy định pháp
					luật.</li>
				<li>Các nghĩa vụ khác theo quy định của Công ty và các quy định có liên quan của pháp luật.</li>
