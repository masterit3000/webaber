			<p>Nội dung của Bản điều kiện và điều khoản sử dụng dịch vụ Công ty dưới đây tạo thành một Hợp đồng sử dụng dịch vụ ràng
				buộc về mặt pháp lý giữa Công ty. Khi Khách hàng sử dụng dịch vụ Công ty có nghĩa là khách hàng đã chấp nhận các Điều
				khoản và Điều kiện dưới đây và chịu sự ràng buộc phải tuân thủ bởi các Điều khoản và Điều kiện đó. Khách hàng có trách
				nhiệm xem xét và đọc kỹ nội dung của Bản điều khoản điều kiện này. Nếu khách hàng không đồng ý với các điều khoản và
				điều kiện này thì nên chấm dứt sử dụng ứng dụng này.</p>
			<p>
				<strong>Điều 1. Mô tả dịch vụ</strong>
			</p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ứng dụng Công ty là Phần mềm công nghệ đặt, quản
				lý và điều phối xe trên nền tảng thiết bị di động, theo đó Người sử dụng dịch vụ vận tải (khách hàng)
				tải ứng dụng Công ty để đặt trước dịch vụ vận tải. Dịch vụ Ứng dụng cho phép khách hàng kết nối với Bên
				cung cấp dịch vụ vận tải khi khách hàng thực hiện giao dịch bằng việc sử dụng ứng dụng thông qua thiết bị
				di động.</p>
			<p>
				<strong>Điều 2. Điều kiện sử dụng</strong>
			</p>
			<ol>
				<li>Chúng tôi có thể chỉnh sửa, cập nhật “Bản điều kiện và điều khoản sử dụng dịch vụ” này bất cứ lúc nào mà không cần báo
					trước. Việc chỉnh sửa này sẽ có hiệu lực ngay khi được cập nhật trên Ứng dụng Công ty. Khách hàng cần thường xuyên kiểm
					tra các thay đổi này, và việc khách hàng tiếp tục sử dụng Ứng dụng này đồng nghĩa với việc đồng ý với các nội dung đã
					thay đổi đó.</li>
				<li>Bằng cách sử dụng Dịch vụ, khách hàng bảo đảm có quyền hợp pháp để chấp thuận và đồng ý với Điều kiện và điều khoản
					sử dụng, bảo đảm rằng khách hàng có đủ quyền, thẩm quyền và năng lực để sử dụng Dịch vụ và để tuân theo Điều kiện
					và điều khoản sử dụng và tham gia vào một thỏa thuận mang tính hợp đồng.&nbsp;&nbsp;&nbsp;</li>
